const { EmbedBuilder, ActionRowBuilder, ButtonBuilder, ButtonStyle } = require('discord.js');

class DailyModManager {
    constructor(client) {
        this.client = client;
        this.config = client.config;
        this.logger = client.logger;
        this.database = client.database;
    }

    // Otomatik g√ºnl√ºk mod se√ßimi
    async selectDailyMods(date = null) {
        try {
            if (!date) {
                date = new Date().toISOString().split('T')[0]; // YYYY-MM-DD
            }

            this.logger.info(`G√ºnl√ºk mod se√ßimi ba≈ülƒ±yor: ${date}`);

            // G√ºnd√ºz i√ßin m√ºsait modlarƒ± al
            const dayMods = await this.database.getAvailableModerators('day');
            // Gece i√ßin m√ºsait modlarƒ± al  
            const nightMods = await this.database.getAvailableModerators('night');

            if (dayMods.length < 2) {
                throw new Error(`G√ºnd√ºz i√ßin yeterli moderat√∂r yok (${dayMods.length}/2)`);
            }

            if (nightMods.length < 2) {
                throw new Error(`Gece i√ßin yeterli moderat√∂r yok (${nightMods.length}/2)`);
            }

            // Rastgele se√ßim yap
            const selectedDayMods = this.selectRandomMods(dayMods, 2);
            const selectedNightMods = this.selectRandomMods(nightMods, 2);

            // Veritabanƒ±na kaydet
            await this.database.saveDailyAssignment(
                date,
                selectedDayMods[0].user_id,
                selectedDayMods[1].user_id,
                selectedNightMods[0].user_id,
                selectedNightMods[1].user_id
            );

            // Admin kanalƒ±na bildir
            await this.announceDailyAssignment(date, selectedDayMods, selectedNightMods);

            // Se√ßilen modlara DM g√∂nder
            await this.notifySelectedMods(date, selectedDayMods, selectedNightMods);

            this.logger.info(`G√ºnl√ºk mod se√ßimi tamamlandƒ±: ${date}`);

            return {
                date,
                dayMods: selectedDayMods,
                nightMods: selectedNightMods
            };

        } catch (error) {
            this.logger.botError(error, 'G√ºnl√ºk mod se√ßimi');
            throw error;
        }
    }

    // Rastgele mod se√ßimi
    selectRandomMods(mods, count) {
        const shuffled = [...mods].sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    }

    // G√ºnl√ºk atamayƒ± admin kanalƒ±nda duyur
    async announceDailyAssignment(date, dayMods, nightMods) {
        try {
            const adminChannel = this.client.channels.cache.get(this.config.discord.adminModChannelId);
            if (!adminChannel) return;

            const embed = new EmbedBuilder()
                .setColor('#00ff00')
                .setTitle('üåÖ G√ºnl√ºk Moderat√∂r Atamasƒ±')
                .setDescription(`**${this.formatDate(date)}** tarihli moderat√∂r atamasƒ±`)
                .addFields(
                    {
                        name: '‚òÄÔ∏è G√ºnd√ºz Vardiyasƒ± (08:00-20:00)',
                        value: dayMods.map((mod, index) => 
                            `**${index + 1}.** <@${mod.user_id}> (${mod.username})`
                        ).join('\n'),
                        inline: false
                    },
                    {
                        name: 'üåô Gece Vardiyasƒ± (20:00-08:00)',
                        value: nightMods.map((mod, index) => 
                            `**${index + 1}.** <@${mod.user_id}> (${mod.username})`
                        ).join('\n'),
                        inline: false
                    }
                )
                .setFooter({ text: 'Otomatik se√ßim sistemi tarafƒ±ndan atanmƒ±≈ütƒ±r.' })
                .setTimestamp();

            const actionRow = new ActionRowBuilder()
                .addComponents(
                    new ButtonBuilder()
                        .setCustomId(`reselect_daily_${date}`)
                        .setLabel('Yeniden Se√ß')
                        .setStyle(ButtonStyle.Secondary)
                        .setEmoji('üîÑ'),
                    new ButtonBuilder()
                        .setCustomId(`manual_assign_${date}`)
                        .setLabel('Manuel Ata')
                        .setStyle(ButtonStyle.Primary)
                        .setEmoji('‚úã')
                );

            await adminChannel.send({
                embeds: [embed],
                components: [actionRow]
            });

        } catch (error) {
            this.logger.error('G√ºnl√ºk atama duyurusu g√∂nderilirken hata:', error.message);
        }
    }

    // Se√ßilen modlara DM g√∂nder
    async notifySelectedMods(date, dayMods, nightMods) {
        const allSelectedMods = [...dayMods, ...nightMods];

        for (const mod of allSelectedMods) {
            try {
                const user = await this.client.users.fetch(mod.user_id);
                const isDayMod = dayMods.some(d => d.user_id === mod.user_id);
                const shift = isDayMod ? 'G√ºnd√ºz (08:00-20:00)' : 'Gece (20:00-08:00)';

                const embed = new EmbedBuilder()
                    .setColor('#0099ff')
                    .setTitle('üëÆ‚Äç‚ôÇÔ∏è Moderat√∂r G√∂revi')
                    .setDescription(`**${this.formatDate(date)}** tarihinde moderat√∂r g√∂revine atandƒ±nƒ±z!`)
                    .addFields({
                        name: 'üìÖ Vardiya Bilgileri',
                        value: `**Tarih:** ${this.formatDate(date)}\n**Vardiya:** ${shift}`,
                        inline: false
                    })
                    .setFooter({ text: 'G√∂revi kabul etmezseniz l√ºtfen admin ekibiyle ileti≈üime ge√ßin.' })
                    .setTimestamp();

                await user.send({ embeds: [embed] });
                this.logger.info(`G√∂rev bildirimi g√∂nderildi: ${mod.username} (${shift})`);

                // Rate limiting
                await new Promise(resolve => setTimeout(resolve, 1000));

            } catch (error) {
                this.logger.error(`${mod.username} kullanƒ±cƒ±sƒ±na DM g√∂nderilemedi:`, error.message);
            }
        }
    }

    // G√ºn√ºn mod atamasƒ±nƒ± getir
    async getTodayAssignment() {
        const today = new Date().toISOString().split('T')[0];
        const assignment = await this.database.getDailyAssignment(today);

        if (!assignment) {
            return null;
        }

        // Moderat√∂r bilgilerini al
        const dayMod1 = await this.getModeratorInfo(assignment.day_mod_1);
        const dayMod2 = await this.getModeratorInfo(assignment.day_mod_2);
        const nightMod1 = await this.getModeratorInfo(assignment.night_mod_1);
        const nightMod2 = await this.getModeratorInfo(assignment.night_mod_2);

        return {
            date: assignment.date,
            dayMods: [dayMod1, dayMod2].filter(Boolean),
            nightMods: [nightMod1, nightMod2].filter(Boolean)
        };
    }

    // Moderat√∂r bilgilerini getir
    async getModeratorInfo(userId) {
        if (!userId) return null;

        const sql = `SELECT * FROM moderators WHERE user_id = ?`;
        
        return new Promise((resolve, reject) => {
            this.database.db.get(sql, [userId], (err, row) => {
                if (err) {
                    reject(err);
                } else {
                    if (row) {
                        resolve({
                            ...row,
                            roles: JSON.parse(row.roles || '[]')
                        });
                    } else {
                        resolve(null);
                    }
                }
            });
        });
    }

    // O an aktif olan modlarƒ± bul
    async getCurrentActiveMods() {
        const now = new Date();
        const currentHour = now.getHours();
        const today = now.toISOString().split('T')[0];

        const assignment = await this.database.getDailyAssignment(today);
        if (!assignment) return [];

        // 08:00-20:00 g√ºnd√ºz, 20:00-08:00 gece
        const isDayTime = currentHour >= 8 && currentHour < 20;

        let activeMods = [];

        if (isDayTime) {
            // G√ºnd√ºz vardiyasƒ±
            const dayMod1 = await this.getModeratorInfo(assignment.day_mod_1);
            const dayMod2 = await this.getModeratorInfo(assignment.day_mod_2);
            activeMods = [dayMod1, dayMod2].filter(Boolean);
        } else {
            // Gece vardiyasƒ±
            const nightMod1 = await this.getModeratorInfo(assignment.night_mod_1);
            const nightMod2 = await this.getModeratorInfo(assignment.night_mod_2);
            activeMods = [nightMod1, nightMod2].filter(Boolean);
        }

        return activeMods.map(mod => ({
            ...mod,
            shift: isDayTime ? 'G√ºnd√ºz' : 'Gece',
            shiftTime: isDayTime ? '08:00-20:00' : '20:00-08:00'
        }));
    }

    // Yeniden se√ßim yap
    async reselectDailyMods(date) {
        try {
            // Eski atamayƒ± sil
            const sql = `DELETE FROM daily_assignments WHERE date = ?`;
            await new Promise((resolve, reject) => {
                this.database.db.run(sql, [date], (err) => {
                    if (err) reject(err);
                    else resolve();
                });
            });

            // Yeniden se√ß
            return await this.selectDailyMods(date);

        } catch (error) {
            this.logger.error('Yeniden se√ßim hatasƒ±:', error.message);
            throw error;
        }
    }

    // Tarih formatla
    formatDate(dateStr) {
        const date = new Date(dateStr + 'T00:00:00');
        return date.toLocaleDateString('tr-TR', {
            weekday: 'long',
            year: 'numeric',
            month: 'long',
            day: 'numeric'
        });
    }

    // Interaction'larƒ± y√∂net
    async handleInteraction(interaction) {
        try {
            if (interaction.isButton() && interaction.customId.startsWith('reselect_daily_')) {
                await this.handleReselect(interaction);
            } else if (interaction.isButton() && interaction.customId.startsWith('manual_assign_')) {
                await this.handleManualAssign(interaction);
            }
        } catch (error) {
            this.logger.error('Daily mod interaction hatasƒ±:', error.message);
        }
    }

    // Yeniden se√ßim buton handler
    async handleReselect(interaction) {
        try {
            await interaction.deferReply({ ephemeral: true });

            const date = interaction.customId.split('_')[2];
            const result = await this.reselectDailyMods(date);

            await interaction.editReply({
                content: `‚úÖ **${this.formatDate(date)}** i√ßin yeniden se√ßim yapƒ±ldƒ±!\n` +
                        `üåÖ G√ºnd√ºz: ${result.dayMods.map(m => m.username).join(', ')}\n` +
                        `üåô Gece: ${result.nightMods.map(m => m.username).join(', ')}`
            });

        } catch (error) {
            await interaction.editReply({
                content: `‚ùå Yeniden se√ßim hatasƒ±: ${error.message}`
            });
        }
    }

    // Manuel atama buton handler
    async handleManualAssign(interaction) {
        await interaction.reply({
            content: 'üìù Manuel atama i√ßin `/admin daily assign` komutunu kullanƒ±n.',
            ephemeral: true
        });
    }
}

module.exports = DailyModManager; 